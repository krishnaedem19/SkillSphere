# ---------------- FUNCTION ---------------- #

def analyze_skills(skills):
    careers, courses, materials, projects = [], [], [], []

    for skill in skills:
        skill = skill.strip()

        if skill in career_data:
            data = career_data[skill]

            careers.extend(data["careers"])
            courses.extend(data["courses"])
            materials.extend(data["materials"])
            projects.extend(data["projects"])

    return careers, courses, materials, projects